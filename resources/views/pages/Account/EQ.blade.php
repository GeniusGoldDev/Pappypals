          <h1>What is EQ?</h1>
          <p>
Kids are so much more than their grades, and we have to educate the whole child. Enhancing skills like problem-solving, creativity, analytical thinking, collaboration, communication, ethics and accountability are considered essential for a child’s future success. All of these skills, and many more, are what we call Social and Emotional Intelligence (EQ/SEL). 

</p>
