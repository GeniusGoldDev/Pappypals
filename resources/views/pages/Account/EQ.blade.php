          <h1>Vad är EQ?</h1>
          <p>
Det räcker inte att lära barn ABC och 123 utan vi måste ha en mer holistisk syn på utbildning och även utbilda barnen i värdegrund. Precis som matte och språk behöver barn öva på de sociala och emotionella färdigheterna. När barn får öva på uthållighet, problemlösning, empati, kreativitet, samarbete, kommunikation m.m. i tidig ålder så lägger vi grunden för deras framtida välmående och framgång. Alla dessa färdigheter och många fler är det som ingår i social och emotionell intelligens (EQ). 

